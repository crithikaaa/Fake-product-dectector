"use client";

import { useEffect, useMemo, useState } from "react";
import Link from "next/link";
import { useRouter } from "next/navigation";
import { generateFakeAnalysis, type FakeAnalysisResult } from "@/lib/fakeAnalysis";
import ResultCard from "@/components/ResultCard";

export default function ResultPage() {
  const router = useRouter();
  const [imageDataUrl, setImageDataUrl] = useState<string | null>(null);

  useEffect(() => {
    const stored = localStorage.getItem("aifpd:lastScan");
    if (!stored) {
      router.replace("/scan");
      return;
    }
    setImageDataUrl(stored);
  }, [router]);

  const result: FakeAnalysisResult = useMemo(() => generateFakeAnalysis(), []);

  if (!imageDataUrl) return null;

  return (
    <main className="relative min-h-screen px-6 py-14">
      <div className="pointer-events-none absolute inset-0 grid-bg" />
      <div className="pointer-events-none absolute inset-0 scanlines" />

      <div className="relative mx-auto max-w-5xl">
        <div className="flex flex-wrap items-end justify-between gap-6">
          <div>
            <h1 className="text-3xl font-semibold tracking-tight">
              Authenticity Report
            </h1>
            <p className="mt-2 text-sm text-white/65">
              Generated by a simulated AI authenticity model (demo prototype).
            </p>
          </div>
          <div className="flex items-center gap-2">
            <Link
              href="/scan"
              className="rounded-xl bg-indigo-500 px-4 py-2 text-xs font-semibold text-white shadow-neon transition hover:bg-indigo-400 focus:outline-none focus:ring-2 focus:ring-indigo-300/60"
            >
              Scan Another Product
            </Link>
            <Link
              href="/"
              className="rounded-xl bg-white/5 px-4 py-2 text-xs font-semibold text-white/75 ring-1 ring-white/10 transition hover:bg-white/10"
            >
              Home
            </Link>
          </div>
        </div>

        <div className="mt-10">
          <ResultCard imageDataUrl={imageDataUrl} result={result} />
        </div>
      </div>
    </main>
  );
}

